public class HelloJava{ // 클래스의 범위 시작

    // 주의! Java 언어는 대소문자 구분한다. 대소문자 다르면 오류 발생. String, System 등 유의해서 작성하자.
    public static void main(String[] args){ // 프로그램의 시작점. main 메서드의 범위 시작.
        System.out.println("Hello Java"); // 값을 콘솔에 출력. 자바 문자열 => 쌍따옴표로 묶기.
    } // main 메서드의 범위 끝.

} // 클래스의 범위 끝